PATCH_B_FAMILY = "student"
BACKGROUND_FAMILY = "gaussian"
BACKGROUND_TAU = 0.10
PATCH_A_SHARE = 0.15
PATCH_B_SHARE = 0.15
BACKGROUND_SHARE = 1.0 - PATCH_A_SHARE - PATCH_B_SHARE
PATCH_TAU_VALUES: tuple[float, float] = (0.4, 0.8)


