def _init_worker() -> None:
  torch.set_default_dtype(torch.float64)
  torch.set_num_threads(1)


# -----------------------------------------------------------------------------
# Geometry helpers
# -----------------------------------------------------------------------------


def _reflect(u: Tensor, rotation: int) -> Tensor:
  if rotation == 90:
    return torch.stack((1.0 - u[:, 0], u[:, 1]), dim=1)
  if rotation == 180:
    return 1.0 - u
  if rotation == 270:
    return torch.stack((u[:, 0], 1.0 - u[:, 1]), dim=1)
  return u


def _corner_count(u: Tensor, q: float, rotation: int) -> int:
  corner = _reflect(u, rotation)
  return int(((corner[:, 0] <= q) & (corner[:, 1] <= q)).sum())


def _corner_mass_single(bicop: pv.Bicop, q: float, rotation: int) -> float:
  def cdf(a: float, b: float) -> float:
    return float(np.asarray(bicop.cdf(np.array([[a, b]])))[0])

  if rotation == 90:
    return q - cdf(1.0 - q, q)
  if rotation == 180:
    return 2.0 * q - 1.0 + cdf(1.0 - q, 1.0 - q)
  if rotation == 270:
    return q - cdf(q, 1.0 - q)
  return cdf(q, q)


def _truth_targets_single(
  bicop: pv.Bicop, u_tail_grid: Tensor, q: float, rotation: int
) -> tuple[Tensor, Tensor, Tensor, float]:
  corner = _reflect(u_tail_grid, rotation)
  c_true = torch.as_tensor(bicop.pdf(corner.numpy()), dtype=torch.float64)
  r_true = q * c_true
  p_true = _corner_mass_single(bicop, q, rotation)
  h_true = (q**2 / p_true) * c_true
  return c_true, r_true, h_true, p_true


# -----------------------------------------------------------------------------
# Patchwork benchmark generation and truth
# -----------------------------------------------------------------------------


def _triangle_transform(sample: np.ndarray, q: float, which: str) -> np.ndarray:
  """Map a [0,1]^2 sample into the lower-left square and then into one triangle.

  ``which == 'A'`` maps to the lower triangle (u2 <= u1).
  ``which == 'B'`` maps to the upper triangle (u2 > u1).
  """
  u = q * sample
  if which == "A":
    return np.sort(u, axis=1)[:, ::-1]
  if which == "B":
    return np.sort(u, axis=1)
  raise ValueError(which)


def sample_patchwork_benchmark(
  n: int,
  seed: int,
  tau_a: float,
  tau_b: float,
  use_pseudo_observations: bool,
) -> np.ndarray:
  """Sample a region-based lower-tail patchwork benchmark.

  The returned sample lives in [0,1]^2. Patch A occupies the lower triangle of
  the lower-tail square, Patch B the upper triangle, and the background fills
  the remaining mass.
  """
  rng = np.random.default_rng(seed)
  labels = rng.choice(
    3,
    size=n,
    p=np.array([BACKGROUND_SHARE, PATCH_A_SHARE, PATCH_B_SHARE], dtype=float),
  )

  u = np.empty((n, 2), dtype=float)

  # Background component over the full square.
  idx_bg = np.where(labels == 0)[0]
  if len(idx_bg) > 0:
    bg = sample_bicop(
      len(idx_bg), seed + 11, BACKGROUND_FAMILY, {"tau": BACKGROUND_TAU}
    )
    u[idx_bg] = bg

  # Patch A: lower triangle of [0, q]^2.
  idx_a = np.where(labels == 1)[0]
  if len(idx_a) > 0:
    a = sample_bicop(len(idx_a), seed + 101, PATCH_A_FAMILY, {"tau": tau_a})
    u[idx_a] = _triangle_transform(a, q=0.10, which="A")

  # Patch B: upper triangle of [0, q]^2.
  idx_b = np.where(labels == 2)[0]
  if len(idx_b) > 0:
    b = sample_bicop(
      len(idx_b), seed + 202, PATCH_B_FAMILY, {"tau": tau_b, "nu": 4}
    )
    u[idx_b] = _triangle_transform(b, q=0.10, which="B")

  if use_pseudo_observations:
    u = rank_to_pseudo_observations(u)

  return u


def _patch_component_density(
  bicop: pv.Bicop,
  u_grid: Tensor,
  q: float,
  triangle: str,
) -> Tensor:
  """Density of a scaled triangle component (approximate but coherent benchmark)."""
  # On the lower-tail square, the component density is the scaled copula density.
  # Sorting into a triangle effectively doubles the density on that triangle.
  u = u_grid.clone()
  if triangle == "A":
    mask = u[:, 1] <= u[:, 0]
  elif triangle == "B":
    mask = u[:, 1] > u[:, 0]
  else:
    raise ValueError(triangle)

  out = torch.zeros(u.shape[0], dtype=torch.float64)
  if mask.any():
    x = u[mask] / q
    out[mask] = (2.0 / (q**2)) * torch.as_tensor(
      bicop.pdf(x.numpy()), dtype=torch.float64
    )
  return out


def _truth_targets_patchwork(
  tau_a: float,
  tau_b: float,
  u_tail_grid: Tensor,
  q: float,
  rotation: int,
) -> tuple[Tensor, Tensor, Tensor, float]:
  """Benchmark truth for the patchwork mode.

  This is the exact density of the benchmark construction used by
  ``sample_patchwork_benchmark``.
  """
  bicop_bg = set_bicop(
    BACKGROUND_FAMILY, copula_params(BACKGROUND_FAMILY, BACKGROUND_TAU)
  )
  bicop_a = set_bicop(PATCH_A_FAMILY, copula_params(PATCH_A_FAMILY, tau_a))
  bicop_b = set_bicop(
    PATCH_B_FAMILY, copula_params(PATCH_B_FAMILY, tau_b, nu=4)
  )

  corner = _reflect(u_tail_grid, rotation)

  c_bg = torch.as_tensor(bicop_bg.pdf(corner.numpy()), dtype=torch.float64)
  c_a = _patch_component_density(bicop_a, corner, q, triangle="A")
  c_b = _patch_component_density(bicop_b, corner, q, triangle="B")

  c_true = BACKGROUND_SHARE * c_bg + PATCH_A_SHARE * c_a + PATCH_B_SHARE * c_b
  r_true = q * c_true

  # Tail mass: background mass in [0,q]^2 plus all of the patch masses.
  def bg_cdf(a: float, b: float) -> float:
    return float(np.asarray(bicop_bg.cdf(np.array([[a, b]])))[0])

  p_bg = bg_cdf(q, q)
  p_true = BACKGROUND_SHARE * p_bg + PATCH_A_SHARE + PATCH_B_SHARE
  h_true = (q**2 / p_true) * c_true
  return c_true, r_true, h_true, p_true


# -----------------------------------------------------------------------------
# Estimator helpers
# -----------------------------------------------------------------------------


def _ordinary_targets(
  u_data: Tensor,
  u_tail_grid: Tensor,
  q: float,
  cell_area_tail: float,
  ridge: float,
  rotation: int,
) -> tuple[Tensor, Tensor, Tensor, float]:
  corner = _reflect(u_tail_grid, rotation)
  c_body = ProbitTLL(ridge=ridge).fit(u_data).evaluate(corner)
  p_body = float(c_body.sum() * cell_area_tail)
  r_body = q * c_body
  h_body = (q**2 / p_body) * c_body
  return c_body, r_body, h_body, p_body


def _metric_rows(
  spec: ComboSpec,
  q: float,
  k: int,
  mode_label: str,
  family_label: str,
  tau_label: float | None,
  tau_a_label: float | None,
  tau_b_label: float | None,
  ests: dict[str, dict[str, Tensor]],
  truths: dict[str, Tensor],
  cell_areas: dict[str, float],
  p_hats: dict[str, float],
  p_true: float,
) -> list[dict[str, object]]:
  base: dict[str, object] = {
    "mode": mode_label,
    "seed": spec.seed,
    "family": family_label,
    "tau": tau_label,
    "tau_a": tau_a_label,
    "tau_b": tau_b_label,
    "n": spec.n,
    "k": k,
    "q": q,
  }
  rows: list[dict[str, object]] = []
  for model, model_ests in ests.items():
    for target, est in model_ests.items():
      metrics = grid_metrics_density(est, truths[target], cell_areas[target])
      rows.append({**base, "model": model, "target": target, **metrics})
  for model, p_hat in p_hats.items():
    rows.append(
      {
        **base,
        "model": model,
        "target": "p",
        "p_hat": p_hat,
        "p_true": p_true,
        "AE": abs(p_hat - p_true),
        "RE": abs(p_hat - p_true) / p_true,
      }
    )
  return rows


