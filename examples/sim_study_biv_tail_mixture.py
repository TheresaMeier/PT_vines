def q_of_n(n: int) -> float:
  return n ** (-0.5)


def copula_params(family: str, tau: float, nu: int = 4) -> dict[str, float]:
  if family == "clayton":
    return {"theta": 2.0 * tau / (1.0 - tau)}
  if family == "gumbel":
    return {"theta": 1.0 / (1.0 - tau)}
  if family in {"gaussian", "student"}:
    params = {"rho": math.sin(math.pi * tau / 2.0)}
    if family == "student":
      params["nu"] = float(nu)
    return params
  raise ValueError(f"Unknown family: {family}")


def set_bicop(family: str, params: dict[str, float]) -> pv.Bicop:
  if family == "clayton":
    return pv.Bicop(
      family=pv.BicopFamily.clayton,  # type: ignore[attr-defined]
      rotation=0,
      parameters=np.array([[params["theta"]]], dtype=float),
    )
  if family == "gumbel":
    return pv.Bicop(
      family=pv.BicopFamily.gumbel,  # type: ignore[attr-defined]
      rotation=0,
      parameters=np.array([[params["theta"]]], dtype=float),
    )
  if family == "gaussian":
    return pv.Bicop(
      family=pv.BicopFamily.gaussian,  # type: ignore[attr-defined]
      parameters=np.array([[params["rho"]]], dtype=float),
    )
  if family == "student":
    return pv.Bicop(
      family=pv.BicopFamily.student,  # type: ignore[attr-defined]
      parameters=np.array([[params["rho"]], [params["nu"]]], dtype=float),
    )
  raise ValueError(f"Unknown family: {family}")


def sample_bicop(
  n: int, seed: int, family: str, spec: dict[str, float]
) -> np.ndarray:
  bicop = set_bicop(family, copula_params(family, **spec))
  return bicop.simulate(n, seeds=[seed])


def rank_to_pseudo_observations(u: np.ndarray) -> np.ndarray:
  """Optional rank transform (not used in single mode; available for experiments)."""
  n = u.shape[0]
  ranks = np.empty_like(u, dtype=float)
  for j in range(u.shape[1]):
    order = np.argsort(u[:, j], kind="mergesort")
    ranks[order, j] = np.arange(1, n + 1, dtype=float)
  return ranks / (n + 1.0)


