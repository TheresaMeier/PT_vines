def _simulate_single(spec: SingleComboSpec) -> ComboResult:
  try:
    params = copula_params(spec.family, spec.tau, spec.nu)
    bicop = set_bicop(spec.family, params)
    u_data = torch.as_tensor(
      bicop.simulate(spec.n, seeds=[spec.seed]), dtype=torch.float64
    )
    q = q_of_n(spec.n)
    k = _corner_count(u_data, q, spec.rotation)
    if k < spec.min_tail_count:
      return ComboResult(
        spec, f"skipped (k={k} < {spec.min_tail_count})", k, []
      )

    grid, cell_area = unit_grid(spec.grid_size, spec.eps)
    u_tail_grid = grid * q
    cell_area_tail = (q**2) * cell_area

    tail = TailCopula(q, rotation=spec.rotation, ridge=spec.ridge).fit(u_data)
    c_true, r_true, h_true, p_true = _truth_targets_single(
      bicop, u_tail_grid, q, spec.rotation
    )
    c_body, r_body, h_body, p_body = _ordinary_targets(
      u_data, u_tail_grid, q, cell_area_tail, spec.ridge, spec.rotation
    )

    ests = {
      "Ordinary": {"r": r_body, "h": h_body, "c": c_body},
      "Tail": {"r": tail.r(grid), "h": tail.h(grid), "c": tail.c(grid)},
    }
    truths = {"r": r_true, "h": h_true, "c": c_true}
    cell_areas = {"r": cell_area, "h": cell_area, "c": cell_area_tail}
    p_hats = {"Ordinary": p_body, "Tail": tail.fit_.p}

    for model, family in PARAMETRIC_MODELS.items():
      try:
        par_est = ParametricTailCopula(family, q, rotation=spec.rotation).fit(
          u_data
        )
      except Exception:  # noqa: BLE001
        continue
      ests[model] = {
        "r": par_est.r(grid),
        "h": par_est.h(grid),
        "c": par_est.c(grid),
      }
      p_hats[model] = par_est.fit_.p

    rows = _metric_rows(
      spec,
      q,
      k,
      mode_label="single",
      family_label=spec.family,
      tau_label=spec.tau,
      tau_a_label=None,
      tau_b_label=None,
      ests=ests,
      truths=truths,
      cell_areas=cell_areas,
      p_hats=p_hats,
      p_true=p_true,
    )
    return ComboResult(spec, "ok", k, rows)
  except torch.linalg.LinAlgError:
    return ComboResult(spec, "skipped (singular bandwidth)", None, [])
  except Exception as exc:  # noqa: BLE001
    return ComboResult(spec, f"error: {exc!r}", None, [])


def _simulate_patchwork(spec: PatchworkSpec) -> ComboResult:
  try:
    q = q_of_n(spec.n)
    u_raw = sample_patchwork_benchmark(
      n=spec.n,
      seed=spec.seed,
      tau_a=spec.tau_a,
      tau_b=spec.tau_b,
      use_pseudo_observations=spec.use_pseudo_observations,
    )
    u = torch.as_tensor(u_raw, dtype=torch.float64)

    k = _corner_count(u, q, rotation=0)
    if k < spec.min_tail_count:
      return ComboResult(
        spec, f"skipped (k={k} < {spec.min_tail_count})", k, []
      )

    grid, cell_area = unit_grid(spec.grid_size, spec.eps)
    u_tail_grid = grid * q
    cell_area_tail = (q**2) * cell_area

    tail = TailCopula(q, rotation=0, ridge=spec.ridge).fit(u)
    c_true, r_true, h_true, p_true = _truth_targets_patchwork(
      spec.tau_a, spec.tau_b, u_tail_grid, q, rotation=0
    )
    c_body, r_body, h_body, p_body = _ordinary_targets(
      u, u_tail_grid, q, cell_area_tail, spec.ridge, rotation=0
    )

    ests = {
      "Ordinary": {"r": r_body, "h": h_body, "c": c_body},
      "Tail": {"r": tail.r(grid), "h": tail.h(grid), "c": tail.c(grid)},
    }
    truths = {"r": r_true, "h": h_true, "c": c_true}
    cell_areas = {"r": cell_area, "h": cell_area, "c": cell_area_tail}
    p_hats = {"Ordinary": p_body, "Tail": tail.fit_.p}

    for model, family in PARAMETRIC_MODELS.items():
      try:
        par_est = ParametricTailCopula(family, q, rotation=0).fit(u)
      except Exception:  # noqa: BLE001
        continue
      ests[model] = {
        "r": par_est.r(grid),
        "h": par_est.h(grid),
        "c": par_est.c(grid),
      }
      p_hats[model] = par_est.fit_.p

    rows = _metric_rows(
      spec,
      q,
      k,
      mode_label="patchwork",
      family_label="",
      tau_label=None,
      tau_a_label=spec.tau_a,
      tau_b_label=spec.tau_b,
      ests=ests,
      truths=truths,
      cell_areas=cell_areas,
      p_hats=p_hats,
      p_true=p_true,
    )
    return ComboResult(spec, "ok", k, rows)
  except torch.linalg.LinAlgError:
    return ComboResult(spec, "skipped (singular bandwidth)", None, [])
  except Exception as exc:  # noqa: BLE001
    return ComboResult(spec, f"error: {exc!r}", None, [])


def run_combo(spec: ComboSpec) -> ComboResult:
  if spec.mode == "single":
    assert isinstance(spec, SingleComboSpec)
    return _simulate_single(spec)
  assert isinstance(spec, PatchworkSpec)
  return _simulate_patchwork(spec)


