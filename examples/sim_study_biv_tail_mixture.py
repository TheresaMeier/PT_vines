"""Parallel bivariate-tail simulation study for the ``npptcop`` package.

This script keeps the original exact single-family benchmark and adds a new
``patchwork`` benchmark designed to stress nonparametric tail estimators.

Modes
-----
- ``single``: exact copula simulation for one family per cell.
- ``patchwork``: a lower-tail patchwork benchmark with two diagonal tail patches
  inside ``[0, q] x [0, q]`` and a weak background component outside.
- ``both``: run both studies and concatenate the results.

Important note about the patchwork mode
---------------------------------------
The patchwork mode is a *benchmark density on the unit square*, not a fully
analytic copula construction. It is intentionally designed to create two
visibly different lower-tail patches so that nonparametric estimators have a
real local-structure advantage to learn.

The data are generated by mixing three region-specific components:
- background component on the whole square,
- Patch A on the lower triangle of the lower-tail square,
- Patch B on the upper triangle of the lower-tail square.

The true target density in patchwork mode is therefore known by construction,
while the actual estimator inputs are the simulated unit-square samples.
"""

